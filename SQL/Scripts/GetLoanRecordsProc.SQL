CREATE PROCEDURE GETLOANRECORDSPROC
(
	@Dealer_ID int
)
AS
BEGIN
	SELECT 
	LOAN_ID as "Loan_ID",
	[YEAR] as "Year",
	MAKE as "Make",
	MODEL as "Model",
	VIN as "VIN",
	LOAN_AMOUNT as "Loan_Amount",
	APR_RATE AS "APR"
	FROM LOAN L
	JOIN VEHICLE V
	ON L.VEHICLE_ID = V.VEHICLE_ID 
	WHERE DEALER_ID = @DEALER_ID
END
